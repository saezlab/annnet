"""Render merged benchmark records into the SSoT Markdown report (+ scaling plots).

Consumes the ``payload`` dict produced by :mod:`benchmarks.run`
(``{environment, config, records}``) and emits a single ``REPORT.md`` plus one
log-log scaling PNG per comparable operation. Everything the report shows is
derived from the records — no measurement happens here.
"""

from __future__ import annotations

from pathlib import Path

COMPARABLE_OPS = ['build', 'degree', 'neighbors', 'has_edge', 'enumerate_edges']
ANNNET_INTERNAL_ORDER = [
    'build_hyperedges',
    'build_multilayer',
    'materialize_incidence_csr',
    'materialize_adjacency',
    'copy',
    'subgraph_half',
    'annotations_vertex_bulk',
    'io_write',
    'io_read',
]
_SCALE_ORDER = {'small': 0, 'medium': 1, 'large': 2, 'xlarge': 3}


# ---------------------------------------------------------------------------
# helpers
# ---------------------------------------------------------------------------
def _median_ms(rec):
    t = rec.get('time')
    return t['median_s'] * 1e3 if t else None


def _fmt_ms(v):
    if v is None:
        return '—'
    if v < 1:
        return f'{v * 1000:.1f} µs'
    if v < 1000:
        return f'{v:.3g} ms'
    return f'{v / 1000:.3g} s'


def _fmt_bytes(n):
    if n is None:
        return '—'
    for unit in ('B', 'KB', 'MB', 'GB'):
        if abs(n) < 1024:
            return f'{n:.1f} {unit}'
        n /= 1024
    return f'{n:.1f} TB'


def _index(records):
    idx = {}
    for r in records:
        idx.setdefault((r['engine'], r['scale'], r['op']), r)
    return idx


def _scales_in(records):
    return sorted({r['scale'] for r in records}, key=lambda s: _SCALE_ORDER.get(s, 99))


def _pick(idx, engine, scale, op):
    return idx.get((engine, scale, op))


# ---------------------------------------------------------------------------
# sections
# ---------------------------------------------------------------------------
def _header(env, cfg):
    libs = env.get('libraries', {})
    lines = [
        '# AnnNet — Benchmark Report',
        '',
        '> Single source of truth for AnnNet speed, memory and overhead. '
        'Generated by `python -m benchmarks.run`. '
        'All timings are per-call medians over warmup + repeated batches with GC '
        'disabled; memory is `tracemalloc` retained allocation.',
        '',
        '## Environment',
        '',
        '| field | value |',
        '|---|---|',
        f'| timestamp (UTC) | {env.get("timestamp_utc")} |',
        f'| git commit | `{env.get("git_commit")}` |',
        f'| python | {env.get("python")} |',
        f'| platform | {env.get("platform")} |',
        f'| processor | {env.get("processor")} |',
        f'| CPUs (logical/affinity) | {env.get("cpu", {}).get("logical")} / '
        f'{env.get("cpu", {}).get("affinity")} |',
        f'| networkx | {libs.get("networkx")} |',
        f'| igraph | {libs.get("igraph")} |',
        f'| numpy / scipy | {libs.get("numpy")} / {libs.get("scipy")} |',
        f'| polars / pandas / pyarrow | {libs.get("polars")} / '
        f'{libs.get("pandas")} / {libs.get("pyarrow")} |',
        '',
        f'**Config:** tier=`{cfg.get("tier")}`, scales=`{cfg.get("scales")}`, '
        f'backends=`{cfg.get("backends")}`.',
        '',
    ]
    return lines


def _head_to_head(idx, records):
    lines = [
        '## Head-to-head (AnnNet vs NetworkX vs igraph)',
        '',
        'Median per-call time on semantically comparable operations. '
        '`ratio` = AnnNet / igraph '
        '(>1 means AnnNet slower). Remember AnnNet carries an incidence '
        'matrix + annotation frames + multilayer state, so parity on plain '
        'graph ops is the honest bar, not a tie.',
        '',
    ]
    for scale in _scales_in(records):
        n_e = next((r['n_edges'] for r in records if r['scale'] == scale), '?')
        lines += [
            f'### scale = {scale}  (~{n_e} edges)',
            '',
            '| op | AnnNet | NetworkX | igraph | ratio (AnN/ig) |',
            '|---|---|---|---|---|',
        ]
        for op in COMPARABLE_OPS:
            a = _pick(idx, 'annnet', scale, op)
            nx = _pick(idx, 'networkx', scale, op)
            ig = _pick(idx, 'igraph', scale, op)
            am, nm, im = (
                _median_ms(a) if a else None,
                _median_ms(nx) if nx else None,
                _median_ms(ig) if ig else None,
            )
            ratio = f'{am / im:.2f}×' if (am and im) else '—'
            lines.append(f'| {op} | {_fmt_ms(am)} | {_fmt_ms(nm)} | {_fmt_ms(im)} | {ratio} |')
        lines.append('')
    return lines


def _memory(idx, records):
    lines = [
        '## Memory footprint',
        '',
        'Retained allocation for the built graph (`tracemalloc`), and '
        'bytes per edge. AnnNet stores endpoints, weights, an incidence '
        'matrix and annotation columns; NetworkX/igraph store adjacency '
        'only.',
        '',
    ]
    for scale in _scales_in(records):
        rows = []
        for engine in ('annnet', 'networkx', 'igraph'):
            r = _pick(idx, engine, scale, 'footprint')
            if not r or not r.get('memory'):
                continue
            m = r['memory']
            n_e = r['n_edges'] or 1
            rows.append(
                (
                    engine,
                    m['retained_bytes'],
                    m['retained_bytes'] / n_e,
                    m['peak_bytes'],
                    m['rss_delta_bytes'],
                )
            )
        if not rows:
            continue
        n_e = next((r['n_edges'] for r in records if r['scale'] == scale), '?')
        lines += [
            f'### scale = {scale}  (~{n_e} edges)',
            '',
            '| engine | retained | bytes/edge | peak | rss Δ |',
            '|---|---|---|---|---|',
        ]
        for engine, ret, bpe, peak, rss in rows:
            lines.append(
                f'| {engine} | {_fmt_bytes(ret)} | {bpe:.1f} | '
                f'{_fmt_bytes(peak)} | {_fmt_bytes(rss)} |'
            )
        lines.append('')
    return lines


def _annnet_internals(idx, records):
    lines = [
        '## AnnNet-only capabilities (expressiveness cost)',
        '',
        'Operations NetworkX and igraph cannot express; there is no baseline '
        'to compare against — these quantify the cost of the features AnnNet '
        'uniquely provides.',
        '',
    ]
    for scale in _scales_in(records):
        body = []
        for op in ANNNET_INTERNAL_ORDER:
            r = _pick(idx, 'annnet', scale, op)
            if not r:
                continue
            note = r.get('note', '')
            disk = r.get('disk_bytes')
            extra = f' · disk {_fmt_bytes(disk)}' if disk else ''
            body.append(f'| {op} | {_fmt_ms(_median_ms(r))} | {note}{extra} |')
        if not body:
            continue
        lines += [f'### scale = {scale}', '', '| op | median | note |', '|---|---|---|']
        lines += body + ['']
    return lines


def _plots(records, plots_dir):
    scales = _scales_in(records)
    if len(scales) < 2:
        return []
    try:
        import matplotlib

        matplotlib.use('Agg')
        import matplotlib.pyplot as plt
    except Exception:
        return []

    idx = _index(records)
    plots_dir.mkdir(parents=True, exist_ok=True)
    lines = [
        '## Scaling curves',
        '',
        'Median time vs edge count (log-log). One curve per engine.',
        '',
    ]
    for op in COMPARABLE_OPS:
        series = {}
        for engine in ('annnet', 'networkx', 'igraph'):
            xs, ys = [], []
            for scale in scales:
                r = idx.get((engine, scale, op))
                if r and r.get('time'):
                    xs.append(r['n_edges'])
                    ys.append(r['time']['median_s'])
            if len(xs) >= 2:
                series[engine] = (xs, ys)
        if not series:
            continue
        fig, ax = plt.subplots(figsize=(5, 3.2))
        for engine, (xs, ys) in series.items():
            ax.plot(xs, ys, marker='o', label=engine)
        ax.set_xscale('log')
        ax.set_yscale('log')
        ax.set_xlabel('edges')
        ax.set_ylabel('median time (s)')
        ax.set_title(op)
        ax.legend()
        ax.grid(True, which='both', ls=':', alpha=0.4)
        fig.tight_layout()
        png = plots_dir / f'{op}.png'
        fig.savefig(png, dpi=110)
        plt.close(fig)
        rel = png.relative_to(plots_dir.parent)
        lines.append(f'![{op}]({rel})')
    lines.append('')
    return lines


def _feature_section(records, group, title, blurb):
    """Per-scale table for a feature group (slices / history / algorithms)."""
    by_scale = {}
    for r in records:
        if r.get('engine') == 'annnet' and r.get('group') == group and r.get('time'):
            by_scale.setdefault(r['scale'], []).append(r)
    if not by_scale:
        return []
    lines = [f'## {title}', '', blurb, '']
    for scale in _scales_in(records):
        body = by_scale.get(scale)
        if not body:
            continue
        lines += [f'### scale = {scale}', '', '| op | median | note |', '|---|---|---|']
        for r in body:
            lines.append(f'| {r["op"]} | {_fmt_ms(_median_ms(r))} | {r.get("note", "")} |')
        lines.append('')
    return lines


def _ms(tstat):
    """Median milliseconds from a bare TimeStat dict (io/adapter records)."""
    return tstat['median_s'] * 1e3 if tstat else None


def _io_formats(payload):
    recs = payload.get('io_formats')
    if not recs:
        return []
    n_e = next((r.get('n_edges') for r in recs if r.get('n_edges')), '?')
    lines = [
        '## IO formats (round-trip)',
        '',
        f'Write / read median time, on-disk size, and edge-count fidelity for a '
        f'~{n_e}-edge graph through the public `annnet.io` facade.',
        '',
        '| format | write | read | size | edges ok | note |',
        '|---|---|---|---|---|---|',
    ]
    for r in recs:
        if r.get('error'):
            lines.append(f'| {r["format"]} | — | — | — | — | ERROR: {r["error"]} |')
            continue
        lines.append(
            f'| {r["format"]} | {_fmt_ms(_ms(r.get("write")))} | {_fmt_ms(_ms(r.get("read")))} '
            f'| {_fmt_bytes(r.get("size_bytes"))} | {"✓" if r.get("edges_ok") else "✗"} '
            f'| {r.get("note", "")} |'
        )
    lines.append('')
    return lines


def _adapters(payload):
    recs = payload.get('adapters')
    if not recs:
        return []
    n_e = next((r.get('n_edges') for r in recs if r.get('n_edges')), '?')
    lines = [
        '## Adapter conversions',
        '',
        f'Export / import median time for the graph-library bridges '
        f'(~{n_e} edges). Uninstalled backends are skipped.',
        '',
        '| adapter | export | import | edges ok | note |',
        '|---|---|---|---|---|',
    ]
    for r in recs:
        if r.get('skipped'):
            lines.append(f'| {r["adapter"]} | skipped | | | {r["skipped"]} |')
            continue
        if r.get('error'):
            lines.append(f'| {r["adapter"]} | — | — | — | ERROR: {r["error"]} |')
            continue
        ok = '✓' if r.get('edges_ok') else ('—' if r.get('direction') == 'export' else '✗')
        lines.append(
            f'| {r["adapter"]} | {_fmt_ms(_ms(r.get("export")))} | {_fmt_ms(_ms(r.get("import")))} '
            f'| {ok} | {r.get("note", "")} |'
        )
    lines.append('')
    return lines


# ---------------------------------------------------------------------------
# entry point
# ---------------------------------------------------------------------------
def render(payload, report_path, plots_dir=None):
    records = payload['records']
    env = payload.get('environment', {})
    cfg = payload.get('config', {})
    report_path = Path(report_path)
    plots_dir = Path(plots_dir) if plots_dir else report_path.parent / 'plots'
    idx = _index(records)

    lines = []
    lines += _header(env, cfg)
    lines += _head_to_head(idx, records)
    lines += _memory(idx, records)
    lines += _annnet_internals(idx, records)
    lines += _feature_section(
        records,
        'slices',
        'Slices (set algebra, presence, induced subgraph)',
        'Slice operations AnnNet uniquely provides; times are AnnNet-only.',
    )
    lines += _feature_section(
        records,
        'history',
        'History (mutation log, snapshots, diff)',
        'Cost of the version log and time-travel features.',
    )
    lines += _feature_section(
        records,
        'algorithms',
        'Algorithms / traversal',
        'Directional neighbor + edge queries and matrix-derived traversals.',
    )
    lines += _feature_section(
        records,
        'layers',
        'Layers (supra index + supra operations)',
        'Vertex-layer index and supra-matrix ops; nl_to_row must stay ~O(1) in the '
        'supra size (cached index).',
    )
    lines += _feature_section(
        records,
        'backend_proxy',
        'Backend proxies (G.nx.*)',
        'Per-call wrapper tax with the converted graph cached; must stay ~O(1) in '
        'vertex count (O(1) output mapping).',
    )
    lines += _io_formats(payload)
    lines += _adapters(payload)
    lines += _plots(records, plots_dir)

    report_path.write_text('\n'.join(lines))
    return report_path
