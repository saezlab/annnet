def _run_commands() -> list[str]:
    return [
        '## Run Commands',
        '',
        '| purpose | command |',
        '|---|---|',
        '| standard full report | `uv run python -m benchmarks.run --tier full --out benchmarks/results/full` |',
        '| graph-tool quick report | `pixi run -e gt benchmark-gt-quick` |',
        '| graph-tool full report | `pixi run -e gt benchmark-gt-full` |',
        '| graph-tool heavy report | `pixi run -e gt benchmark-gt-heavy` |',
        '| graph-tool huge report (tiny to xlarge) | `pixi run -e gt benchmark-gt-huge` |',
        '| spec-sheet PDF | `uv run python -m benchmarks.reporting.specsheet` |',
        '| regenerate reports from JSON | `uv run python -m benchmarks.reporting.regenerate` |',
        '',
        'The graph-tool commands use the Pixi `gt` environment because graph-tool is '
        'distributed through conda-forge, not PyPI.',
        '',
    ]


def _summary(records: list[dict], plots_dir: Path) -> list[str]:
    scales = scales_in(records)
    comparable = engines_in(records, ops=COMPARABLE_OPS)
    timed = [r for r in records if r.get('time')]
    memory = [r for r in records if r.get('memory')]
    skipped = [r for r in records if r.get('status') == 'skipped']
    graph_tool = 'yes' if 'graph-tool' in comparable else 'not in comparable records'

    lines = [
        '## Summary',
        '',
        '| item | value |',
        '|---|---|',
        f'| records | {len(records)} total, {len(timed)} timed, {len(memory)} memory, {len(skipped)} skipped |',
        f'| scales | {", ".join(scales) if scales else "-"} |',
        f'| comparable engines | {", ".join(engine_label(e) for e in comparable) or "-"} |',
        f'| graph-tool comparable run | {graph_tool} |',
        '',
    ]
    lines += _ratio_snapshot(records)
    lines += _plot_shortlist(records, plots_dir)
    return lines


def _ratio_snapshot(records: list[dict]) -> list[str]:
    idx = index_records(records)
    scales = scales_in(records)
    if not scales:
        return []
    scale = scales[-1]
    baselines = [
        engine for engine in BASELINE_ENGINES if engine in engines_in(records, ops=COMPARABLE_OPS)
    ]
    if not baselines:
        return []
    lines = [
        f'### Largest Scale Snapshot ({scale})',
        '',
        '| op | ' + ' | '.join(ratio_label(engine) for engine in baselines) + ' |',
        '|---' + '|---' * len(baselines) + '|',
    ]
    for op in ('build', 'neighbors', 'has_edge', 'enumerate_edges'):
        ann = median_ms(pick(idx, 'annnet', scale, op))
        ratios = []
        for baseline in baselines:
            base = median_ms(pick(idx, baseline, scale, op))
            ratios.append(f'{ann / base:.2f}x' if (ann and base) else '-')
        lines.append(f'| {op} | ' + ' | '.join(ratios) + ' |')
    lines.append('')
    return lines


def _plot_shortlist(records: list[dict], plots_dir: Path) -> list[str]:
    if len(scales_in(records)) < 2:
        return []
    links = [
        ('Comparable operation scaling', plots_dir / 'scaling_wall_time_overview.png'),
        ('Retained memory scaling', plots_dir / 'memory_retained_scaling.png'),
        ('Memory density', plots_dir / 'memory_bytes_per_edge.png'),
    ]
    for baseline in BASELINE_ENGINES:
        if baseline in engines_in(records, ops=COMPARABLE_OPS, require_metric=True):
            links.insert(
                1,
                (
                    f'{ratio_label(baseline)} heatmap',
                    plots_dir / f'annnet_vs_{slug(baseline)}_ratio_heatmap.png',
                ),
            )
    lines = ['### Key Plots', '', '| plot | path |', '|---|---|']
    for label, path in links:
        lines.append(f'| {label} | `{rel(path, plots_dir.parent)}` |')
    lines.append('')
    return lines


def _head_to_head(idx: dict, records: list[dict]) -> list[str]:
    engines = engines_in(records, ops=COMPARABLE_OPS)
    if not engines:
        return []
    baselines = [engine for engine in BASELINE_ENGINES if engine in engines]
    baseline_text = ', '.join(engine_label(engine) for engine in baselines) or 'installed baselines'
    lines = [
        f'## Head-to-head (AnnNet vs {baseline_text})',
        '',
        'Median per-call time on semantically comparable operations. Ratio columns are '
        'AnnNet divided by the named baseline; values above 1x mean AnnNet is slower.',
        '',
    ]
    value_columns = [engine_label(engine) for engine in engines]
    ratio_columns = [ratio_label(engine) for engine in baselines]
    for scale in scales_in(records):
        n_e = next((r['n_edges'] for r in records if r['scale'] == scale), '?')
        lines += [
            f'### scale = {scale} (~{n_e} edges)',
            '',
            '| op | ' + ' | '.join(value_columns + ratio_columns) + ' |',
            '|---' + '|---' * (len(value_columns) + len(ratio_columns)) + '|',
        ]
        for op in COMPARABLE_OPS:
            ann_ms = median_ms(pick(idx, 'annnet', scale, op))
            values = [fmt_ms(median_ms(pick(idx, engine, scale, op))) for engine in engines]
            ratios = []
            for baseline in baselines:
                base_ms = median_ms(pick(idx, baseline, scale, op))
                ratios.append(f'{ann_ms / base_ms:.2f}x' if (ann_ms and base_ms) else '-')
            lines.append(f'| {op} | ' + ' | '.join(values + ratios) + ' |')
        lines.append('')
    return lines


def _memory(idx: dict, records: list[dict]) -> list[str]:
    engines = engines_in(records, ops=['footprint'], require_metric=True)
    if not engines:
        return []
    lines = [
        '## Memory Footprint',
        '',
        'Retained allocation for the built graph and retained bytes per edge. AnnNet '
        'stores endpoints, weights, incidence/matrix state, and annotation columns; '
        'the external graph libraries store adjacency-oriented representations.',
        '',
    ]
    for scale in scales_in(records):
        rows = []
        for engine in engines:
            r = pick(idx, engine, scale, 'footprint')
            if not r or not r.get('memory'):
                continue
            m = r['memory']
            n_e = r['n_edges'] or 1
            rows.append(
                (
                    engine,
                    m['retained_bytes'],
                    m['retained_bytes'] / n_e,
                    m['peak_bytes'],
                    m['rss_delta_bytes'],
                )
            )
        if not rows:
            continue
        n_e = next((r['n_edges'] for r in records if r['scale'] == scale), '?')
        lines += [
            f'### scale = {scale} (~{n_e} edges)',
            '',
            '| engine | retained | bytes/edge | peak | rss delta |',
            '|---|---|---|---|---|',
        ]
        for engine, ret, bpe, peak, rss in rows:
            lines.append(
                f'| {engine_label(engine)} | {fmt_bytes(ret)} | {bpe:.1f} | '
                f'{fmt_bytes(peak)} | {fmt_bytes(rss)} |'
            )
        lines.append('')
    return lines


def _annnet_internals(idx: dict, records: list[dict]) -> list[str]:
    lines = [
        '## AnnNet-only Capabilities',
        '',
        'Operations the external graph libraries cannot express directly. These are '
        'feature costs, not baseline comparisons.',
        '',
    ]
    for scale in scales_in(records):
        body = []
        for op in ANNNET_INTERNAL_ORDER:
            r = pick(idx, 'annnet', scale, op)
            if not r:
                continue
            note = r.get('note', '')
            disk = r.get('disk_bytes')
            extra = f' ; disk {fmt_bytes(disk)}' if disk else ''
            body.append(f'| {op} | {fmt_ms(median_ms(r))} | {note}{extra} |')
        if not body:
            continue
        lines += [f'### scale = {scale}', '', '| op | median | note |', '|---|---|---|']
        lines += body + ['']
    return lines


def _feature_section(records: list[dict], group: str, title: str, blurb: str) -> list[str]:
    by_scale = {}
    for r in records:
        if r.get('engine') == 'annnet' and r.get('group') == group and r.get('time'):
            by_scale.setdefault(r['scale'], []).append(r)
    if not by_scale:
        return []
    lines = [f'## {title}', '', blurb, '']
    for scale in scales_in(records):
        body = by_scale.get(scale)
        if not body:
            continue
        lines += [f'### scale = {scale}', '', '| op | median | note |', '|---|---|---|']
        for r in body:
            lines.append(f'| {r["op"]} | {fmt_ms(median_ms(r))} | {r.get("note", "")} |')
        lines.append('')
    return lines


def _extra_dimensions(records: list[dict]) -> list[str]:
    lines = []
    for group, title, blurb in EXTRA_GROUPS:
        by_scale = {}
        for r in records:
            if r.get('group') == group:
                by_scale.setdefault(r['scale'], []).append(r)
        if not by_scale:
            continue
        lines += [f'## {title}', '', blurb, '']
        for scale in scales_in(records):
            body = by_scale.get(scale)
            if not body:
                continue
            lines += [
                f'### scale = {scale}',
                '',
                '| engine | op | median | status | note |',
                '|---|---|---|---|---|',
            ]
            for r in body:
                median = fmt_ms(median_ms(r)) if r.get('time') else '-'
                status = r.get('status', 'ok' if r.get('time') else 'skipped')
                note = r.get('note', '')
                if r.get('reason'):
                    note = f'{note}; {r["reason"]}' if note else r['reason']
                if r.get('error'):
                    note = f'{note}; ERROR: {r["error"]}' if note else f'ERROR: {r["error"]}'
                lines.append(
                    f'| {engine_label(r["engine"])} | {r["op"]} | {median} | {status} | {note} |'
                )
            lines.append('')
    return lines


def _io_formats(payload: dict) -> list[str]:
    recs = payload.get('io_formats')
    if not recs:
        return []
    n_e = next((r.get('n_edges') for r in recs if r.get('n_edges')), '?')
    lines = [
        '## IO Formats',
        '',
        f'Write/read median time, on-disk size, and edge-count fidelity for a ~{n_e}-edge graph.',
        '',
        '| format | write | read | size | edges ok | note |',
        '|---|---|---|---|---|---|',
    ]
    for r in recs:
        if r.get('error'):
            lines.append(f'| {r["format"]} | - | - | - | - | ERROR: {r["error"]} |')
            continue
        lines.append(
            f'| {r["format"]} | {fmt_ms(ms(r.get("write")))} | {fmt_ms(ms(r.get("read")))} '
            f'| {fmt_bytes(r.get("size_bytes"))} | {"yes" if r.get("edges_ok") else "no"} '
            f'| {r.get("note", "")} |'
        )
    lines.append('')
    return lines


def _adapters(payload: dict) -> list[str]:
    recs = payload.get('adapters')
    if not recs:
        return []
    n_e = next((r.get('n_edges') for r in recs if r.get('n_edges')), '?')
    lines = [
        '## Adapter Conversions',
        '',
        f'Export/import median time for graph-library bridges (~{n_e} edges). '
        'Uninstalled backends are skipped.',
        '',
        '| adapter | export | import | edges ok | note |',
        '|---|---|---|---|---|',
    ]
    for r in recs:
        if r.get('skipped'):
            lines.append(f'| {r["adapter"]} | skipped | | | {r["skipped"]} |')
            continue
        if r.get('error'):
            lines.append(f'| {r["adapter"]} | - | - | - | ERROR: {r["error"]} |')
            continue
        ok = 'yes' if r.get('edges_ok') else ('-' if r.get('direction') == 'export' else 'no')
        lines.append(
            f'| {r["adapter"]} | {fmt_ms(ms(r.get("export")))} | {fmt_ms(ms(r.get("import")))} '
            f'| {ok} | {r.get("note", "")} |'
        )
    lines.append('')
    return lines
