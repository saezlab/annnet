        '`python -m benchmarks.run` from recorded JSON results.',
        '',
        '## Environment',
        '',
        '| field | value |',
        '|---|---|',
        f'| timestamp UTC | {env.get("timestamp_utc")} |',
        f'| git commit | `{env.get("git_commit")}` |',
        f'| python | {env.get("python")} |',
        f'| platform | {env.get("platform")} |',
        f'| processor | {env.get("processor")} |',
        f'| CPUs logical/affinity | {env.get("cpu", {}).get("logical")} / '
        f'{env.get("cpu", {}).get("affinity")} |',
        f'| networkx | {libs.get("networkx")} |',
        f'| igraph | {libs.get("igraph")} |',
        f'| graph-tool | {libs.get("graph_tool")} |',
        f'| numpy / scipy | {libs.get("numpy")} / {libs.get("scipy")} |',
        f'| polars / pandas / pyarrow | {libs.get("polars")} / '
        f'{libs.get("pandas")} / {libs.get("pyarrow")} |',
