"""Markdown renderer for the benchmark report."""

from __future__ import annotations

from pathlib import Path

from .plots import (
    artifact_index,
    render_extra_plots,
    render_comparable_plots,
    render_io_adapter_plots,
)
from .common import (
    EXTRA_GROUPS,
    COMPARABLE_OPS,
    BASELINE_ENGINES,
    ANNNET_INTERNAL_ORDER,
    ms,
    rel,
    pick,
    slug,
    fmt_ms,
    fmt_bytes,
    median_ms,
    scales_in,
    engines_in,
    ratio_label,
    engine_label,
    index_records,
)


def render(payload: dict, report_path, plots_dir=None):
    records = payload['records']
    env = payload.get('environment', {})
    cfg = payload.get('config', {})
    report_path = Path(report_path)
    plots_dir = Path(plots_dir) if plots_dir else report_path.parent / 'plots'
    _clear_generated_artifacts(plots_dir)
    idx = index_records(records)

    lines = []
    lines += _header(env, cfg)
    lines += _run_commands()
    lines += _summary(records, plots_dir)
    lines += _head_to_head(idx, records)
    lines += _memory(idx, records)
    lines += _annnet_internals(idx, records)
    lines += _feature_section(
        records,
        'slices',
        'Slices',
        'Set algebra, presence checks, and induced subgraphs. These are AnnNet-only timings.',
    )
    lines += _feature_section(
        records,
        'history',
        'History',
        'Mutation-log, snapshot, and diff costs.',
    )
    lines += _feature_section(
        records,
        'algorithms',
        'Algorithms / Traversal',
        'Directional neighbor and edge queries plus matrix-derived traversal helpers.',
    )
    lines += _extra_dimensions(records)
    lines += render_comparable_plots(records, plots_dir)
    lines += render_extra_plots(records, plots_dir)
    lines += _io_formats(payload)
    lines += _adapters(payload)
    lines += render_io_adapter_plots(payload, plots_dir)
    lines += artifact_index(records, plots_dir)

    report_path.write_text('\n'.join(lines))
    return report_path


def _clear_generated_artifacts(plots_dir: Path) -> None:
    plots_dir.mkdir(parents=True, exist_ok=True)
    for pattern in ('*.png', 'benchmark_records.csv', 'status_counts.csv'):
        for path in plots_dir.glob(pattern):
            path.unlink()


